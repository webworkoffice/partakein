package in.partake.model.dto;

import in.partake.model.dto.auxiliary.ParticipationStatus;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Id;

public class ArrangementEnrollment {
    @Id
    private String arrangementId;       // どの arrangement への参加表明か
    @Id
    private String userId;              // 誰の    
    @Column
    private String comment;             // コメント
    @Column
    private ParticipationStatus status; // 参加ステータス
    @Column
    private Date enrolledAt;            // enroll した時刻
}
